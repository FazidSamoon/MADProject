package com.example.madproject;

import java.util.Date;

public class Workout {

    String workout1;
    String workout2;
    String workout3;
    String workoutdes;
    Date date;


    public Workout() {
    }

    public String getWorkout1() {
        return workout1;
    }

    public void setWorkout1(String workout1) {
        this.workout1 = workout1;
    }

    public String getWorkout2() {
        return workout2;
    }

    public void setWorkout2(String workout2) {
        this.workout2 = workout2;
    }

    public String getWorkout3() {
        return workout3;
    }

    public void setWorkout3(String workout3) {
        this.workout3 = workout3;
    }

    public String getWorkoutdes() {
        return workoutdes;
    }

    public void setWorkoutdes(String workoutdes) {
        this.workoutdes = workoutdes;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
}

